import Foundation


class Pessoa {
    
    var nome: String
    var cpf: String
    var celular: String
    var cep: String
    var email: String
    
    init(nome: String, cpf: String, celular: String, cep: String, email: String) {
        self.nome = nome
        self.cpf = cpf
        self.celular = celular
        self.cep = cep
        self.email = email
        
    }
    
  
    
}
